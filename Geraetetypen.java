package Backend;

public class Geraetetypen {
	
	
	private int geraettyp_nr;
	private String bezeichnung;
	private String kategorie;
	private int hersteller_nr;
	
	public int getGeraettyp_nr() {
		return geraettyp_nr;
	}
	public void setGeraettyp_nr(int geraettyp_nr) {
		this.geraettyp_nr = geraettyp_nr;
	}
	public String getBezeichnung() {
		return bezeichnung;
	}
	public void setBezeichnung(String bezeichnung) {
		this.bezeichnung = bezeichnung;
	}
	public String getKategorie() {
		return kategorie;
	}
	public void setKategorie(String kategorie) {
		this.kategorie = kategorie;
	}
	public int getHersteller_nr() {
		return hersteller_nr;
	}
	public void setHersteller_nr(int hersteller_nr) {
		this.hersteller_nr = hersteller_nr;
	}
	
}
